public class math
{
public static void main(String[] args)
{
    //math class
    int a =20;
    int b=30;
    System.out.println(Math.max(a,b)); // Math.max results out the max in the two number
    System.out.println(Math.min(a,b)); // Math.min results out the min in the two number

    //random -> helps in selecting random values3
    System.out.println(Math.random()); //helps in getting random value b/w 0.0 to 1.0
    System.out.println((int)Math.random());//if we want random value in form of integer
    System.out.println((int)(Math.random()*100));//get random value between 1 to 100
}}
